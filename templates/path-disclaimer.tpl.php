<h2>Customizations</h2>

<p>You can customize the disclaimer per url and path by creating different template names.</p>

<p>Example for the page <code>node/1</code></p>

<ul>
  <li>path-disclaimer--node-1.tpl.php</li>
</ul>

<p>Or if you want to customize the disclaimer per url and per language</p>

<ul>
  <li>path-disclaimer--node-1--fr.tpl.php</li>
</ul>
